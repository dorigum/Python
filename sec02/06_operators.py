# 연산자
# 산술 연산자 : +, -, *, /, //(나눗셈, 몫), %(나머지), **(지수)
# 관계 연산자 : ==, !=, >, <, >=, <=
# 논리 연산자 : and, or, not
# 비트 연산자 : & (AND), | (OR), ^ (XOR), ~ (NOT)

print(20 // 7) # 나눗셈 (몫)
print(20 % 7) # 나머지
print(2 ** 3) # 지수

# 연습문제
# 현금 5,000원 있고
# 사탕 가격이 120원
# 최대한 살 수 있는 사탕의 개수와 나머지 돈은 얼마인가?
money = 5000
candy = 120

# 최대한 살 수 있는 사탕의 수 (나누기(몫))
maxcandy = money // candy

# 최대한 사탕을 구입하고 남은 돈 (나머지)
change = money % candy
print("%d개, 남은 돈 : %d원" % (maxcandy, change))